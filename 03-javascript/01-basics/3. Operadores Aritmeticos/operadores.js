// Definimos dos números base
let a = 10;
let b = 3;

// 1. Operadores Básicos
console.log("Suma (10 + 3):", a + b);         // 13
console.log("Resta (10 - 3):", a - b);        // 7
console.log("Multiplicación (10 * 3):", a * b); // 30
console.log("División (10 / 3):", a / b);       // 3.3333...

// 2. Módulo (%) - Es el RESTO de una división
// Útil para saber si un número es par o impar
console.log("Módulo (10 % 3):", a % b);       // 1 (Porque 3*3 es 9, sobra 1)

// 3. Exponenciación (**) - Potencia
console.log("Potencia (10 elevado a 3):", a ** b); // 1000

// 4. Incremento y Decremento
let contador = 5;
contador++; // Equivale a: contador = contador + 1
console.log("Incremento (5++):", contador);   // 6

contador--; // Equivale a: contador = contador - 1
console.log("Decremento (6--):", contador);   // 5